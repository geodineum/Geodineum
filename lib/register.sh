# Detect dominant programming language from project files
express_detect_language() {
    local path="$1"

    # Check for definitive project files first
    [[ -f "${path}/composer.json" ]] && { echo "php"; return 0; }
    [[ -f "${path}/wp-config.php" ]] && { echo "php"; return 0; }
    [[ -f "${path}/Cargo.toml" ]] && { echo "rust"; return 0; }
    [[ -f "${path}/go.mod" ]] && { echo "go"; return 0; }
    [[ -f "${path}/pyproject.toml" ]] && { echo "python"; return 0; }
    [[ -f "${path}/requirements.txt" ]] && { echo "python"; return 0; }
    [[ -f "${path}/setup.py" ]] && { echo "python"; return 0; }
    [[ -f "${path}/package.json" ]] && { echo "node"; return 0; }
    [[ -f "${path}/Gemfile" ]] && { echo "ruby"; return 0; }
    [[ -f "${path}/build.gradle" ]] || [[ -f "${path}/pom.xml" ]] && { echo "java"; return 0; }

    # Fall back to file extension counting (depth-limited)
    local counts=()
    local php_n py_n js_n rs_n go_n
    php_n=$(find "$path" -maxdepth 3 -name "*.php" 2>/dev/null | head -100 | wc -l)
    py_n=$(find "$path" -maxdepth 3 -name "*.py" 2>/dev/null | head -100 | wc -l)
    js_n=$(find "$path" -maxdepth 3 \( -name "*.js" -o -name "*.ts" \) -not -path "*/node_modules/*" 2>/dev/null | head -100 | wc -l)
    rs_n=$(find "$path" -maxdepth 3 -name "*.rs" 2>/dev/null | head -100 | wc -l)
    go_n=$(find "$path" -maxdepth 3 -name "*.go" 2>/dev/null | head -100 | wc -l)

    local max=0 lang="unknown"
    for pair in "php:${php_n}" "python:${py_n}" "node:${js_n}" "rust:${rs_n}" "go:${go_n}"; do
        local l="${pair%%:*}" n="${pair##*:}"
        if [[ $n -gt $max ]]; then
            max=$n
            lang="$l"
        fi
    done

    echo "$lang"
}

# Detect framework from project files
express_detect_framework() {
    local path="$1"
    local lang="$2"

    case "$lang" in
        php)
            [[ -f "${path}/wp-config.php" ]] || [[ -f "${path}/wp-load.php" ]] && { echo "wordpress"; return 0; }
            grep -q '"laravel/framework"' "${path}/composer.json" 2>/dev/null && { echo "laravel"; return 0; }
            grep -q '"symfony/framework-bundle"' "${path}/composer.json" 2>/dev/null && { echo "symfony"; return 0; }
            grep -q '"slim/slim"' "${path}/composer.json" 2>/dev/null && { echo "slim"; return 0; }
            echo "php-generic"
            ;;
        python)
            grep -qi "fastapi" "${path}/requirements.txt" "${path}/pyproject.toml" 2>/dev/null && { echo "fastapi"; return 0; }
            grep -qi "django" "${path}/requirements.txt" "${path}/pyproject.toml" 2>/dev/null && { echo "django"; return 0; }
            grep -qi "flask" "${path}/requirements.txt" "${path}/pyproject.toml" 2>/dev/null && { echo "flask"; return 0; }
            echo "python-generic"
            ;;
        node)
            grep -q '"next"' "${path}/package.json" 2>/dev/null && { echo "nextjs"; return 0; }
            grep -q '"express"' "${path}/package.json" 2>/dev/null && { echo "express"; return 0; }
            grep -q '"fastify"' "${path}/package.json" 2>/dev/null && { echo "fastify"; return 0; }
            grep -q '"@nestjs/core"' "${path}/package.json" 2>/dev/null && { echo "nestjs"; return 0; }
            echo "node-generic"
            ;;
        rust)
            grep -q 'axum' "${path}/Cargo.toml" 2>/dev/null && { echo "axum"; return 0; }
            grep -q 'actix' "${path}/Cargo.toml" 2>/dev/null && { echo "actix"; return 0; }
            grep -q 'rocket' "${path}/Cargo.toml" 2>/dev/null && { echo "rocket"; return 0; }
            grep -q 'tokio' "${path}/Cargo.toml" 2>/dev/null && { echo "rust-async"; return 0; }
            echo "rust-generic"
            ;;
        go)
            grep -q 'gin-gonic' "${path}/go.mod" 2>/dev/null && { echo "gin"; return 0; }
            grep -q 'labstack/echo' "${path}/go.mod" 2>/dev/null && { echo "echo"; return 0; }
            grep -q 'go-chi/chi' "${path}/go.mod" 2>/dev/null && { echo "chi"; return 0; }
            echo "go-generic"
            ;;
        *)
            echo "generic"
            ;;
    esac
}

# Detect API protocol from project structure
express_detect_protocol() {
    local path="$1"
    local framework="$2"

    # Framework-specific defaults
    case "$framework" in
        wordpress|laravel|symfony|slim|django|flask|express|fastify|nextjs|nestjs|gin|echo|chi|axum|actix|rocket)
            echo "http_rest"
            return 0
            ;;
    esac

    # Check for GraphQL
    if find "$path" -maxdepth 3 \( -name "*.graphql" -o -name "schema.graphqls" \) 2>/dev/null | head -1 | grep -q .; then
        echo "graphql"
        return 0
    fi

    # Check for gRPC / protobuf
    if find "$path" -maxdepth 3 -name "*.proto" 2>/dev/null | head -1 | grep -q .; then
        echo "grpc"
        return 0
    fi

    # Check for stream-based (gNode integration)
    if grep -rq "xadd\|XADD\|xreadgroup\|XREADGROUP\|gnode_stream" "$path" --include="*.php" --include="*.py" --include="*.rs" --include="*.go" --include="*.js" -l 2>/dev/null | head -1 | grep -q .; then
        echo "gnode_stream"
        return 0
    fi

    echo "http_rest"
}

# Detect service scope from project structure
express_detect_scope() {
    local path="$1"
    local framework="$2"

    # WordPress and web frameworks are client-facing
    case "$framework" in
        wordpress|laravel|django|nextjs|symfony)
            echo "client_facing"
            return 0
            ;;
    esac

    # Check for systemd service (daemon)
    if find /etc/systemd/system/ -name "*.service" -exec grep -l "$path" {} \; 2>/dev/null | head -1 | grep -q .; then
        echo "daemon"
        return 0
    fi

    # Check for queue/worker patterns
    if grep -rq "consumer\|worker\|queue\|celery\|supervisor" "$path" --include="*.py" --include="*.php" --include="*.yaml" --include="*.conf" -l 2>/dev/null | head -1 | grep -q .; then
        echo "worker"
        return 0
    fi

    # Check for CLI tool
    if [[ -f "${path}/bin/console" ]] || [[ -d "${path}/cli" ]]; then
        echo "worker"
        return 0
    fi

    # Default for web-accessible paths
    if [[ "$path" == /var/www/* ]]; then
        echo "client_facing"
        return 0
    fi

    echo "daemon"
}

# Detect log file location
express_detect_logs() {
    local path="$1"

    for candidate in \
        "${path}/storage/logs" \
        "${path}/var/log" \
        "${path}/logs" \
        "${path}/log" \
        "/var/log/$(basename "$path")"; do
        if [[ -d "$candidate" ]]; then
            echo "$candidate"
            return 0
        fi
    done

    echo ""
}

# Detect documentation location
express_detect_docs() {
    local path="$1"

    for candidate in \
        "${path}/docs" \
        "${path}/doc" \
        "${path}/documentation" \
        "${path}/wiki"; do
        if [[ -d "$candidate" ]]; then
            echo "$candidate"
            return 0
        fi
    done

    # Check for README
    if [[ -f "${path}/README.md" ]] || [[ -f "${path}/README.rst" ]]; then
        echo "${path}"
        return 0
    fi

    echo ""
}

# Detect native data format
express_detect_format() {
    local path="$1"
    local framework="$2"

    case "$framework" in
        wordpress|laravel|django|flask|express|fastify|nextjs|nestjs)
            echo "json"
            return 0
            ;;
    esac

    # Check for XML-heavy codebases
    local xml_n json_n
    xml_n=$(find "$path" -maxdepth 2 -name "*.xml" -not -path "*/.git/*" 2>/dev/null | head -20 | wc -l)
    json_n=$(find "$path" -maxdepth 2 -name "*.json" -not -path "*/.git/*" -not -path "*/node_modules/*" 2>/dev/null | head -20 | wc -l)
    if [[ $xml_n -gt $json_n ]] && [[ $xml_n -gt 5 ]]; then
        echo "xml"
        return 0
    fi

    echo "json"
}

