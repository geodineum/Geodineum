#!/bin/bash
#
# Geodineum CLI — Register Command
# ==================================
# Single registration authority for the entire ecosystem.
# Creates .geodineum/ in the service root, wraps onboard-service.sh
# (ACL + streams + registry), and generates gnode_services.yaml for
# daemon capability discovery.
#
# This is the ONLY registration path. Theme-side registration is removed.
# See the registration-flow architecture documentation for rationale.
#
# Requires: common.sh sourced first
#

# =============================================================================
# YAML Format Detection & Conversion
# =============================================================================

# Detect if a YAML file is in flat format (wp-config) or daemon format (gnode_services)
detect_yaml_format() {
    local file="$1"
    if grep -q "^services:" "$file" 2>/dev/null; then
        echo "daemon"
    elif grep -q "^capabilities:" "$file" 2>/dev/null; then
        echo "flat"
    else
        echo "unknown"
    fi
}

# Extract flat capabilities from wp-config-geodineum.yaml → shell variables
extract_flat_capabilities() {
    local file="$1"

    # Parse YAML capabilities section into key=value pairs
    # Only read lines under capabilities: until next top-level key
    local in_caps=false
    while IFS= read -r line; do
        # Start capturing at capabilities:
        if [[ "$line" =~ ^capabilities: ]]; then
            in_caps=true
            continue
        fi
        # Stop at next top-level key (no leading whitespace)
        if [[ "$in_caps" == "true" ]] && [[ "$line" =~ ^[a-z] ]]; then
            break
        fi
        # Skip comments and empty lines
        if [[ "$in_caps" == "true" ]]; then
            local trimmed
            trimmed=$(echo "$line" | sed 's/^\s*//' | sed 's/#.*//' | tr -d '"' | tr -d "'")
            [[ -z "$trimmed" ]] && continue
            # Parse key: value
            local key val
            key=$(echo "$trimmed" | cut -d: -f1 | tr -d ' ')
            val=$(echo "$trimmed" | cut -d: -f2- | sed 's/^\s*//')
            [[ -z "$key" || -z "$val" ]] && continue
            echo "${key}=${val}"
        fi
    done < "$file"
}

# Extract metadata fields (site_id, description, type, tier)
extract_metadata() {
    local file="$1"
    local in_meta=false
    while IFS= read -r line; do
        if [[ "$line" =~ ^metadata: ]]; then
            in_meta=true
            continue
        fi
        if [[ "$in_meta" == "true" ]] && [[ "$line" =~ ^[a-z] ]]; then
            break
        fi
        if [[ "$in_meta" == "true" ]]; then
            local trimmed
            trimmed=$(echo "$line" | sed 's/^\s*//' | sed 's/#.*//' | tr -d '"' | tr -d "'")
            [[ -z "$trimmed" ]] && continue
            local key val
            key=$(echo "$trimmed" | cut -d: -f1 | tr -d ' ')
            val=$(echo "$trimmed" | cut -d: -f2- | sed 's/^\s*//')
            [[ -z "$key" || -z "$val" ]] && continue
            echo "${key}=${val}"
        fi
    done < "$file"
}

# Generate gnode_services.yaml from CAP_* variables
generate_services_yaml() {
    local site_id="$1"
    local output_file="$2"
    local description="${3:-Geodineum service}"
    local tier="${4:-SERVICE}"
    local service_type="${5:-service}"
    local lang="${6:-}"
    # Reads CAP_* variables from environment

    cat > "$output_file" << YAMLEOF
# gNode Service Configuration
# Generated by: geodineum register ${site_id}
# Auto-generated from capabilities — edit to refine.
# The daemon discovers this file via discovery-paths.conf.
# Changes detected within ~120s (or restart daemon).

services:
  - id: "${site_id}"
    metadata:
      description: "${description}"
      type: "${service_type}"
      tier: "${tier}"${lang:+
      language: "${lang}"}
      created_by: "geodineum-register"
    capabilities:
      - name: "protocol"
        value: "${CAP_PROTOCOL:-http_rest}"
      - name: "native_format"
        value: "${CAP_FORMAT:-json}"
      - name: "api_version"
        value: "${CAP_API_VERSION:-v1}"
      - name: "contract_stability"
        value: "${CAP_STABILITY:-stable}"
      - name: "clearance_required"
        value: "${CAP_CLEARANCE:-public}"
      - name: "auth_method"
        value: "${CAP_AUTH:-session_cookie}"
      - name: "data_sensitivity"
        value: "${CAP_SENSITIVITY:-internal}"
      - name: "service_scope"
        value: "${CAP_SCOPE:-client_facing}"
      - name: "domain_primary"
        value: "${CAP_DOMAIN:-content}"
      - name: "domain_secondary"
        value: "${CAP_DOMAIN_SECONDARY:-template}"
      - name: "specialization"
        value: "${CAP_SPECIALIZATION:-generalist}"
      - name: "throughput_tier"
        value: "${CAP_THROUGHPUT:-standard}"
      - name: "latency_class"
        value: "${CAP_LATENCY:-responsive}"
      - name: "reliability_tier"
        value: "${CAP_RELIABILITY:-standard}"
      - name: "pipeline_stage"
        value: "${CAP_PIPELINE_STAGE:-deliver}"
      - name: "execution_priority"
        value: "${CAP_PRIORITY:-normal}"
      - name: "service_tier"
        value: "${tier}"
      - name: "environment"
        value: "${CAP_ENVIRONMENT:-production}"
YAMLEOF
}

